     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $account = new Account;
        $account->nama = $request->nama;
        $account->saldo = $request->saldo;
        $account->norek = $request->norek;
        $account->save();
        return new AccountResource($account);
    }

